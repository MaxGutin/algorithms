function selectionSort($array) {
    $count= count($array);              //запоминаем количество элементов
    if ($count <= 1) return $array;     //если массив пусто или состоит из одного элеммента, то заверщаем программу
    echo print_r($array) . '<br>';
    for ($i = 0; $i < $count; $i++){    //будем проверять каждый элемент массива
        $k = $i;                        //$i — место в которое будем вставлять наименьшее значение
        //$k — индекс начального элемента который будем сравнивать со следующим элементом
        echo 'I — i = ' . $i . '<br>';  //СЛУЖЕБНЫЕ

        for($j = $i + 1; $j < $count; $j++){    //сравниваем значения начального и следующего элементов

            echo $j . ') k = ' . $array[$k] . ' j = ' . $array[$j] . '<br>';    //СЛУЖЕБНЫЕ
            echo '— k = ' . $k . ' j = ' . $j . '<br><br>';                     //СЛУЖЕБНЫЕ

            if ($array[$k] > $array[$j]){       //если значение следующего меньше начального то
                $k = $j;                        //... то запонинаем индекс следующего и повтояем цикл
            }                                   //если же значение начального меньше следующего то повторяем цикл
        }

        if ($k != $i){  //найдя элемент с минимальным значением, ставим его в начало массива
                $tmp = $array[$i];          //сохраняем значение начального элемента
                $array[$i] = $array[$k];    //на его место ставим значение текущего элемента
                $array[$k] = $tmp;          //а на его место ставим значение начального
        }

        echo print_r($array) . '<br>';      //СЛУЖЕБНЫЕ
    }

return $array;
}
