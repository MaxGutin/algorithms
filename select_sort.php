 со значением следующего элемента (j).
Если значение следующего элемента окажется
меньше то мы запомним номер его позиции в переменной k, увеличим значение j на один (j++) и повторим сравнение с уже следующим за ним элементом,
и так пока не пройдем по всему массиву.
После того как внутренний цикл пройдётся по массиву во второй переменной останется номер позиции эленмента
